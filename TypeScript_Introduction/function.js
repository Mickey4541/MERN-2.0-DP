//regular function:::::::::::::::::::::::
function add(a, b) {
    return a + b;
}
console.log(add(5, 5));
//arrow function::::::::::::::::::::::
var multiply = function (a, b) {
    return a * b;
};
var result = multiply(5, 5);
console.log(result);
var division = function (a, b) { return a / b; };
var result1 = division(1, 2);
